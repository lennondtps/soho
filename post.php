            <h3>Review: More Mythos, Mystery and Death with Pretty Deadly #4</h3>
            <div class="post-text">
                <p>Picking up after issue #3’s unsettling revelation of Sissy’s birth, Pretty Deadly #4 immediately kicks into gear as the rogues and not-quite-heroes of this haunting western tale unite to save Sissy. Writer Kelly Sue DeConnick delivers another air-tight script with this issue, brought to the page by artist Emily Rios and her beautiful sense of energy and space. Jordie Bellaire returns to round out this impressive creative team with her subtle color palettes, lending depth and softness to Rios’ otherworldly locales and delicate, fine-featured characters.</p>
                <p>Opening the issue with Bones Bunny and Butterfly, Johnny and Molly arrive at the river’s edge to pull Sissy from the water. In a scene that continues to play with the idea of animal forms, switching back and forth between Molly and Johnny’s respective namesakes, he explains (in part) the story behind Death’s binder. As they set off, we see Alice return to the underworld in her butterfly form, finding Death none too pleased with her. She promises to ally herself with Ginny to deliver Sissy, the Ascendant, to him, and he warns her not to disappoint him again as he restores her body. Alone again, Ginny’s mother, Fox’s wife and Death’s prisoner in the underworld, appeals to him for her release. Too in love to let her go, he promises he will free the both of them instead once he has Sissy.</p>
